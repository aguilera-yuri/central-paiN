const paineis = ['lol_principal', 'lol_academy', 'cs_principal', 'cs_academy', 'kings', 'gta'];

function criarCard(jogador) {
    let img = '<img src="' + jogador.foto + '">';
    let icone = '<img src="' + jogador.icone + '">';
    let bandeira = '<img src="' + jogador.bandeira + '">';

    let card = '<div class="jogador-card">';
    card += '<div class="foto-jogador">' + img + '</div>';
    card += '<div class="infos-jogador">';
    card += '<div class="bandeira">' + bandeira + '</div>';
    card += '<span class="nome-jogador">' + jogador.nome + '</span>';
    card += '<span class="icon-role">' + icone + '</span>';
    card += '</div></div>';
    return card;
}

function preencherGrid(elemento, vetor) {
    let card = "";
    for (let i = 0; i < vetor.length; i++) {
        card += criarCard(vetor[i]);
    }
    elemento.innerHTML = card;
}

