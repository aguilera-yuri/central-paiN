function trocarModalidade(ativa) {

    painel_lol_principal.style.display = 'none';
    btn_lol_principal.style.cssText = estiloInativo;

    painel_lol_academy.style.display = 'none';
    btn_lol_academy.style.cssText = estiloInativo;

    painel_cs_principal.style.display = 'none';
    btn_cs_principal.style.cssText = estiloInativo;

    painel_cs_academy.style.display = 'none';
    btn_cs_academy.style.cssText = estiloInativo;

    painel_kings.style.display = 'none';
    btn_kings.style.cssText = estiloInativo;

    painel_gta.style.display = 'none';
    btn_gta.style.cssText = estiloInativo;

    if (ativa == 'lol_principal') {
        painel_lol_principal.style.display = 'block';
        btn_lol_principal.style.cssText = estiloAtivo;
    }
    else if (ativa == 'lol_academy') {
        painel_lol_academy.style.display = 'block';
        btn_lol_academy.style.cssText = estiloAtivo;
    }
    else if (ativa == 'cs_principal') {
        painel_cs_principal.style.display = 'block';
        btn_cs_principal.style.cssText = estiloAtivo;
    }
    else if (ativa == 'cs_academy') {
        painel_cs_academy.style.display = 'block';
        btn_cs_academy.style.cssText = estiloAtivo;
    }
    else if (ativa == 'kings') {
        painel_kings.style.display = 'block';
        btn_kings.style.cssText = estiloAtivo;
    }
    else if (ativa == 'gta') {
        painel_gta.style.display = 'block';
        btn_gta.style.cssText = estiloAtivo;
    }
}